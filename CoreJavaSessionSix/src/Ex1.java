//DataTypes-->memory size --> Default Value 
public class Ex1 {
	 static byte b; //-> 1 Byte -> 0
	 static short s; //-> 2 Byte -> 0
	 static int i; //-> 4 Byte -> 0
	 static float f; //-> 4 Byte -> 0.0
	 static long l; //-> 8 Byte -> 0
	 static double d; //-> 8 Byte -> 0.0
	 static char c; //-> 2 Byte -> empty space
	 static boolean bool; //-> N/A -> false 
	 static String str; //-> N/A -> null
	public static void main(String[] args) {
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(bool);
		System.out.println(str);
		
	}
}
